package Converters;

public class CurrencyConverter
{
    public double dollarToInr(int dollar) {
        return (dollar * (double) 79.90);
    }
    public double dollarToInr(float dollar) {
        return (dollar * (double) 79.90);
    }
    public double dollarToInr(double dollar) {
        return (dollar * (double) 79.90);
    }
    
    public double inrToDollar(int inr) {
        return (inr / (double) 79.90);
    }
    public double inrToDollar(float inr) {
        return (inr / (double) 79.90);
    }
    public double inrToDollar(double inr) {
        return (inr / (double) 79.90);
    }

    public double euroToInr(int euro) {
        return (euro * (double) 81.09);
    }
    public double euroToInr(float euro) {
        return (euro * (double) 81.09);
    }
    public double euroToInr(double euro) {
        return (euro * (double) 81.09);
    }
    
    public double inrToEuro(int inr) {
        return (inr / (double) 81.09);
    }
    public double inrToEuro(float inr) {
        return (inr / (double) 81.09);
    }
    public double inrToEuro(double inr) {
        return (inr / (double) 81.09);
    }

    public double yenToInr(int yen) {
        return (yen / (double) 1.73);
    }
    public double yenToInr(float yen) {
        return (yen / (double) 1.73);
    }
    public double yenToInr(double yen) {
        return (yen / (double) 1.73);
    }
    
    public double inrToYen(int inr) {
        return (inr * (double) 1.73);
    }
    public double inrToYen(float inr) {
        return (inr * (double) 1.73);
    }
    public double inrToYen(double inr) {
        return (inr * (double) 1.73);
    }
}

